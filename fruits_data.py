def get_fruit_info(fruit_name):
    # Normalize input
    key = fruit_name.lower().strip()
    return fruit_prices.get(key)

def get_all_fruits():
    return list(fruit_prices.keys())

def get_random_fruit():
    import random
    
    # Rarity weights (approximate game chances)
    # Common: 50%, Uncommon: 25%, Rare: 15%, Legendary: 7%, Mythical: 3%
    rarity_weights = {
        "Common": 50,
        "Uncommon": 25,
        "Rare": 15,
        "Legendary": 7,
        "Mythical": 3
    }
    
    # 1. Roll for rarity
    rarities = list(rarity_weights.keys())
    weights = list(rarity_weights.values())
    chosen_rarity = random.choices(rarities, weights=weights, k=1)[0]
    
    # 2. Pick a random fruit from that rarity
    candidates = [name for name, info in fruit_prices.items() if info["rarity"] == chosen_rarity]
    if not candidates:
        # Fallback if no fruits in that rarity (stats are robust but just in case)
        return random.choice(list(fruit_prices.keys()))
        
    return random.choice(candidates)

def calculate_trade_value(fruit_names_str):
    """
    Takes a comma-separated string of fruit names (e.g. "dragon, venom")
    Returns:
      - total_value (int): Sum of Beli values
      - valid_fruits (list): List of recognized fruit names found
      - unrecognized (list): List of unrecognized terms
    """
    total_value = 0
    valid_fruits = []
    unrecognized = []
    
    if not fruit_names_str:
        return 0, [], []
        
    # Split by comma, strip whitespace
    raw_names = [n.strip() for n in fruit_names_str.split(",")]
    
    for name in raw_names:
        if not name: continue
        
        info = get_fruit_info(name)
        if info:
            total_value += info["beli"]
            valid_fruits.append(name.title())
        else:
            # Try fuzzy match? For now, strict or simple contains
            # Let's do a simple fuzzy check if not found
            found = False
            for k in fruit_prices.keys():
                if name.lower() in k:
                    total_value += fruit_prices[k]["beli"]
                    valid_fruits.append(k.title())
                    found = True
                    break
            
            if not found:
                unrecognized.append(name)
                
    return total_value, valid_fruits, unrecognized
